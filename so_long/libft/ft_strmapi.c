#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*newstr;
	unsigned int	i;

	if (!s || !f)
		return (0);
	newstr = (char *) malloc((ft_strlen(s) +1) * sizeof(char));
	i = 0;
	if (!newstr)
		return (NULL);
	if (f && s)
	{
		while (s[i])
		{
			newstr[i] = f(i, s[i]);
			i++;
		}
		newstr[i] = '\0';
		return (newstr);
	}
	return (NULL);
}
